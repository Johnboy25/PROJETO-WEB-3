package model;

public class Produto {
    private int id;
    private String nome;
    private String descricao;
    private String preco;
    private String estoque;
    private int categoria;

    public Produto(int id, String nome, String descricao, String preco, String estoque) {
        this.id = id;
        this.nome = nome;
        this.descricao = descricao;
        this.preco = preco;
        this.estoque = estoque;
        this.categoria = id;
        
    }

    public Produto(int id, String nome, String descricao, String preco, String estoque, int categoria) {
        this.id = id;
        this.nome = nome;
        this.descricao = descricao;
        this.preco = preco;
        this.estoque = estoque;
        this.categoria = categoria;
    }

    public Produto (String nome, String descricao, String preco, String estoque, int categoria) {
        this.nome = nome;
        this.descricao = descricao;
        this.preco = preco;
        this.estoque = estoque;
        this.categoria = categoria;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getPreco() {
        return preco;
    }

    public void setPreco(String preco) {
        this.preco = preco;
    }

    public String getEstoque() {
        return estoque;
    }

    public void setEstoque(String estoque) {
        this.estoque = estoque;
    }

    public int getCategoria() {
        return categoria;
    }

    public void setCategoria(int categoria) {
        this.categoria = categoria;
    }

    @Override
    public String toString() {
        return "Pedido{" + "id=" + id + ", nome=" + nome + ", descricao=" + descricao + ", preco=" + preco + ", estoque=" + estoque + ", categoria=" + categoria + '}';
    }

   
        


}